export const getRandomQuote = (): ScriptureQuote => {
  return scriptureQuotes[Math.floor(Math.random() * scriptureQuotes.length)];
};

export const getQuoteByContext = (situation: string): ScriptureQuote | null => {
  const relevantQuotes = scriptureQuotes.filter(quote => 
    quote.relevantSituation.includes(situation)
  );
  
  if (relevantQuotes.length === 0) return null;
  return relevantQuotes[Math.floor(Math.random() * relevantQuotes.length)];
};

export const getQuotesBySource = (source: string): ScriptureQuote[] => {
  return scriptureQuotes.filter(quote => 
    quote.source.toLowerCase().includes(source.toLowerCase())
  );
};